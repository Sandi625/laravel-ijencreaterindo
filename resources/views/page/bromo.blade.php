            <h3>Itinerary</h3>
            <h4>DAY 1: Pick Up from Bali to Ijen Crater</h4>
            <p>
                Prepare everything and be ready to start the trip. Our driver will pick you up at 07:00 PM from your hotel. You will be taken to Gilimanuk Ferry Port, where you will meet a professional guide. Board the ferry to cross from Bali to Java Island, a journey that will take around 7 to 8 hours in total.
            </p>
            <p>
                Arrive at Ijen Crater and start your hike at 02:00 AM. Explore the beautiful Blue Fire, volcanic activity, and Ijen Sunrise until 07:00 AM. After your hike, enjoy a breakfast for about an hour before continuing your journey to the Tumpak Sewu homestay.
            </p>

            <h4>DAY 2: Tumpak Sewu</h4>
            <p>
                At 12:00 PM, have lunch before arriving at Tumpak Sewu Homestay. Check in and rest, as you will continue your adventure early the next morning. Set your alarm for 04:00 AM to prepare your luggage.
            </p>
            <p>
                By 05:00 AM, get ready to visit the beautiful Tumpak Sewu Waterfall to enjoy the sunrise and panoramic views. Return to the homestay at 08:00 AM for breakfast, then proceed to the Bromo homestay. Check in and rest for the next day’s trip.
            </p>

            <h4>DAY 3: Bromo</h4>
            <p>
                Set your alarm for before 03:00 AM and get ready for the next exploration. Ride a 4WD Jeep to Mount Penanjakan to catch the stunning sunrise from the viewpoint. The journey to the sunrise viewpoint takes about 1 hour, and you’ll enjoy the sea of sand and potentially fog-covered views.
            </p>
            <p>
                After witnessing the sunrise and capturing the beauty of Mount Semeru, head to the peak of Mount Bromo. Climb approximately 250 steps to the summit for breathtaking views of the active volcano. This experience will conclude around 08:00 AM.
            </p>
            <p>
                Return to the hotel, enjoy a delicious breakfast, and depart at 09:00 AM to head back to Banyuwangi. The journey takes around 7 to 8 hours, including lunch, arriving in the city around 04:00 PM. From there, travel to Ketapang Ferry Port for the ferry ride back to Bali, which takes about 1 hour.
            </p>
            <p>
                Upon arrival in Bali, expect another 4-5 hours of travel to reach Denpasar, Ubud, Kuta, or Sanur. (Note that Bali is 1 hour ahead of Java time.) Arrival at your final destination will be around 06:00 PM to 07:00 PM.
